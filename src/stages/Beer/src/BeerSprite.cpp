void BeerSprite::draw()
{
    switch (state)
    {
    case BeerState::FULL:
        arduboy->ardBitmapDrawBitmap(x, y, BEER_FULL, 30, 30, color, ALIGN_NONE, mirror);
        break;
    case BeerState::TOP:
        arduboy->ardBitmapDrawBitmap(x, y, BEER_TOP, 30, 30, color, ALIGN_NONE, mirror);
        break;
    case BeerState::HALF:
        arduboy->ardBitmapDrawBitmap(x, y, BEER_HALF, 30, 30, color, ALIGN_NONE, mirror);
        break;
    case BeerState::BOTTOM:
        arduboy->ardBitmapDrawBitmap(x, y, BEER_BOTTOM, 30, 30, color, ALIGN_NONE, mirror);
        break;
    case BeerState::EMPTY:
        arduboy->ardBitmapDrawBitmap(x, y, BEER_EMPTY, 30, 30, color, ALIGN_NONE, mirror);
        break;
    }
}